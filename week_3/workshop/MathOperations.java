package week_3.workshop;


public class MathOperations
{
    public static void main(String[] args){
        int n1 = 90;
        int n2 = 5;
        
        float f1 = 3.5f;
        float f2 = 4.1f;
        
        System.out.println("Int operations");
        System.out.println("Sum: " + (n1 + n2));
        System.out.println("Difference: " + (n1 - n2));
        System.out.println("Product: " + (n1 * n2));
        System.out.println("Division: " + (n1 / n2));
        
        System.out.println("Float operations");
        System.out.println("Sum: " + (f1 + f2));
        System.out.println("Difference: " + (f1 - f2));
        System.out.println("Product: " + (f1 * f2));
        System.out.println("Division: " + (f1 / f2));
        
    }
}