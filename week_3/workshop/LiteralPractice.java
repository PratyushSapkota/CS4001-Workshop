package week_3.workshop;

public class LiteralPractice
{
    public static void main(String[] args){
        long longLiteral = 1043233324423L;
        float floatLiteral = 4.232f;
        char charLiteral = '\u00A9';

        System.out.println("Long: " + longLiteral);
        System.out.println("Float: " + floatLiteral);
        System.out.println("Char: " + charLiteral);

    }
}
